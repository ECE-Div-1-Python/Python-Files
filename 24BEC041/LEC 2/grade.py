a=int(input("enter the marks between 0 to 100: "))
if(0<=a<=39):
    print("F")
elif(40<=a<=44):
    print("P")
elif(45<=a<=49):
    print("C")
elif(50<=a<=54):
    print("B")
elif(55<=a<=59):
    print("B+")
elif(60<=a<=69):
    print("A")
elif(70<=a<=79):
    print("A+")
elif(80<=a<=100):
    print("O")
else:
    print("NA")


